def main():
    print("=" * 55)
    print("  食物营养数据库导入（NHC指南附录2）")
    print("=" * 55)

    app = create_app("development")
    with app.app_context():
        db.create_all()
        print("[OK] 表结构就绪")

        # 统计
        existing = set(r[0] for r in db.session.query(FoodItem.name).all())
        added = skipped = 0

        for (name, category, cal, prot, fat, carbs, fiber) in FOOD_DATA:
            if name in existing:
                skipped += 1
                continue
            db.session.add(FoodItem(
                name=name,
                category=category,
                calories_per_100g=cal,
                protein_per_100g=prot,
                fat_per_100g=fat,
                carbs_per_100g=carbs,
                fiber_per_100g=fiber,
            ))
            existing.add(name)
            added += 1

        db.session.commit()
        total = FoodItem.query.count()
        print(f"[OK] 新增: {added} 条  |  跳过: {skipped} 条  |  总计: {total} 条")

        # 分类统计
        print("\n分类统计:")
        cats = db.session.query(
            FoodItem.category,
            db.func.count(FoodItem.id)
        ).group_by(FoodItem.category).all()
        for cat, cnt in cats:
            print(f"  {cat}: {cnt} 种")

        print("\n" + "=" * 55)
        print("  导入完成！")
        print("=" * 55)


if __name__ == "__main__":
    main()
